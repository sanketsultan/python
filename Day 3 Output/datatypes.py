#Data types in python 

days_in_week = 7 #this is a integer data type
pi = 3.14 #this is a float data type
name = "John" #this is a string data type
is_sunny = True #this is a boolean data type
fruits = ["apple", "banana", "cherry"] #this is a list data type

#if we want to know the data type of a variable we can use the type() function in python
print(type(days_in_week)) #output: <class 'int'>
print(type(pi)) #output: <class 'float'>
print(type(name)) #output: <class 'str'>
print(type(is_sunny)) #output: <class 'bool'>
print(type(fruits)) #output: <class 'list'>
